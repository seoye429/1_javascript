//1)커지는 함수
function bigImg(x){
    x.style.height = "164px";
    x.style.width = "164px";
}
//2)작아지는 함수
function normalImg(x){
    x.style.height = "32px"; //js에서 css 스타일을 준것
    x.style.width = "32px";
}
